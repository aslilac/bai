use anyhow::anyhow;

pub fn expand_group<S: AsRef<str>>(group: S) -> anyhow::Result<&'static [&'static str]> {
	match group.as_ref() {
		"/gleam" => Ok(&[
			".gitignore",
			"LICENSE",
			"gleam.toml",
			"README.md",
			"src/$$name$$.gleam",
			"test/$$name$$_test.gleam",
			"test/$$name$$/example_test.gleam",
		]),
		"/go" => Ok(&[
			".gitignore",
			"LICENSE",
			"go.mod",
			"main.go",
			"README.md",
			"staticcheck.conf",
		]),
		"/kt" | "kotlin" => Ok(&[]),
		"/rs" | "/rust" => Ok(&[
			".cargo/config.toml",
			".gitignore",
			".rustfmt.toml",
			"Cargo.toml",
			"LICENSE",
			"README.md",
			"rust-toolchain.toml",
			"src/main.rs",
			"tests/main.rs",
			"tests/setup.rs",
		]),
		"/swift" => Ok(&[]),
		"/ts" | "/typescript" => Ok(&[
			".gitignore",
			".prettierignore",
			".prettierrc.json",
			"LICENSE",
			"src/index.ts",
			"package.json",
			"README.md",
			"tsconfig.json",
		]),
		"/react" | "/tsx" => Ok(&[
			".gitignore",
			".prettierignore",
			".prettierrc.json@react",
			"CODE_OF_CONDUCT.md",
			"LICENSE",
			"package.json@react",
			"README.md",
			"src/index.css",
			"src/index.html",
			"src/index.tsx",
			"tsconfig.json@react",
		]),
		any_other_group => Err(anyhow!("unrecognized group: {}", any_other_group)),
	}
}
